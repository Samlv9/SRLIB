var gulp = require("gulp");
var sourcemaps = require("gulp-sourcemaps");
var reference = require("./toolkit/reference");


gulp.task("default", function () {
    gulp.src("./src/main.js")
        .pipe( sourcemaps.init() )
        .pipe( reference() )
        .pipe( sourcemaps.write(".") )
        .pipe( gulp.dest("./dist") );
});

